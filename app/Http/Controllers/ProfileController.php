    {
        $allFiles = $request->all();
        $img = $allFiles['myfile'][0]->store('public');
        $resume = $allFiles['myfile'][1]->store('public');
        Profile::create(['img'=>$img, 'resume'=>$resume, 'user_id'=>Auth::id()]);

        \DB::table('skills')->insert(['name' => $allFiles['skill'], 'user_id'=> Auth::id()]);
        return redirect('profiles/create/');
    }

